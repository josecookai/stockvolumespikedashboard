// Volume ranges by tier (daily USD volume)
const TIER_VOLUME: Record<number, [number, number]> = {
  1: [1_000_000_000, 8_000_000_000],
  2: [100_000_000,   1_200_000_000],
  3: [20_000_000,    150_000_000],
  4: [5_000_000,     30_000_000],
};

// Deterministic pseudo-random based on symbol string
function seededRandom(symbol: string, offset = 0): number {
  let h = offset * 31;
  for (let i = 0; i < symbol.length; i++) h = (h * 31 + symbol.charCodeAt(i)) & 0xffffffff;
  return ((h >>> 0) % 1000) / 1000;
}

export interface MockStockRow {
  symbol: string;
  name: string;
  market: 'stock';
  price: number;
  price_change_pct: number;
  volume_usd: number;
  rel_volume: number;
  market_share_pct: number;
  sparkline: number[];
}

export function generateSP500MockData(totalMarketVolume = 476_000_000_000): MockStockRow[] {
  return SP500.map((s) => {
    const r0 = seededRandom(s.symbol, 0);
    const r1 = seededRandom(s.symbol, 1);
    const r2 = seededRandom(s.symbol, 2);
    const r3 = seededRandom(s.symbol, 3);

    const [volMin, volMax] = TIER_VOLUME[s.tier];
    const volume_usd = volMin + r0 * (volMax - volMin);

    const rel_volume = 0.6 + r1 * 2.4; // 0.6x – 3.0x avg

    // Price: rough approximation by tier
    const basePrices: Record<number, [number, number]> = {
      1: [150, 900], 2: [30, 500], 3: [10, 200], 4: [5, 80],
    };
    const [pMin, pMax] = basePrices[s.tier];
    const price = pMin + r2 * (pMax - pMin);

    const price_change_pct = -8 + r3 * 16; // -8% to +8%

    // Sparkline: 7 values trending toward current volume
    const sparkline = Array.from({ length: 7 }, (_, i) => {
      const trend = seededRandom(s.symbol, i + 10);
      return Math.max(0.1, volume_usd * (0.6 + trend * 0.8) * (0.85 + i * 0.025));
    });

    return {
      symbol: s.symbol,
      name: s.name,
      market: 'stock' as const,
      price: Math.round(price * 100) / 100,
      price_change_pct: Math.round(price_change_pct * 100) / 100,
      volume_usd: Math.round(volume_usd),
      rel_volume: Math.round(rel_volume * 10) / 10,
      market_share_pct: Math.round((volume_usd / totalMarketVolume) * 10000) / 100,
      sparkline: sparkline.map((v) => Math.round(v)),
    };
  });
}
